<h3>Zeno Karl Schindler/MINIARE Fellow<br/> (Research Assistant, non-invasive
analysis of illuminated manuscripts)</h3><p class="db_byline"><p>&pound;24,289 - &pound;27,318</p>
<p>Nine-months contract starting October 2014</p>
<p>The Fitzwilliam Museum houses the principal collections of art and antiquities of the
University of Cambridge, and holds over half a million objects in its care. The Department of
Manuscripts and Printed Books (MSSPB) preserves one of the finest collections of
illuminated manuscripts in the world, one of the most important collections of manuscript and
printed music in the UK, fine printed books, literary autographs and archives. The collections
total upwards of 132,500 objects: 1,000 illuminated manuscripts and leaves; 1,500 music
volumes; 20,000 rare printed books, over 80,000 literary autographs and correspondence,
and over 30,000 items in the Museum&rsquo;s archive.</p></p><p><p>The successful candidate, supported by the MINIARE Fellowship of the Zeno Karl Schindler
Foundation, will join the cross-disciplinary team of the research project MINIARE
(Manuscript Illumination: Non-Invasive Analysis, Research and Expertise, <a href="http://www.miniare.org">www.miniare.org</a>).
Based at the University of Cambridge, MINIARE focuses on the non-invasive analysis of
materials and techniques as well as the contextual interpretation of medieval and
Renaissance illumination.</p>
<p>Under the supervision of MINIARE&rsquo;s pigment analyst and imaging scientist, the Research
Assistant will analyse medieval and Renaissance illuminated manuscripts with a
combination of non-invasive analytical tools, and contribute to the research underpinning a
major exhibition in 2016 which will celebrate the bicentenary of the Fitzwilliam Museum&rsquo;s
foundation. He/she will be trained in the combined, mutually complementary use of different
imaging and analytical methods, and become fully aware of the conservation needs of
unique and exceptionally fragile manuscripts, as well as of the broader cultural and historical
questions that such cross-disciplinary research endeavours to clarify.</p>
<p>Candidates should hold a Bachelor&rsquo;s or Master&rsquo;s Degree in physics, chemistry, materials
science or cultural heritage science. Those who have recently completed a PhD in any of
these fields will also be considered. They should be familiar with a range of analytical and
technical imaging methods (see Further information).</p>
<p>A collaborative, supportive and flexible team working style is essential. The ability to deal
with people at all levels and in a professional manner is crucial. A high degree of IT literacy,
preferably including the use of software for data acquisition and analysis and image
processing, is required. A strong interest in art and art conservation would be an advantage.</p>
