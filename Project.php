                <p>
                    <span class="GoldText">MINIARE</span> is a new interdisciplinary project using a combination of advanced scientific methods for the study of illuminated manuscripts.
                    It will undertake non-invasive analysis of Western, Egyptian, Byzantine, Slavonic, Armenian, Persian, Mughal, Ottoman, Sanskrit and Tibetan illuminated manuscripts (1350 BC - 19th c. AD) and a selection of early printed material.
                    The scientific results will inform studies of the artistic, cultural, political, social and economic environments in which the manuscripts were created, taking into account trade routes, social and international mobility, intellectual and technological developments. 
                    Making the most of Cambridge's exceptional collections and multidisciplinary expertise, MINIARE will bring the sciences, arts and humanities together in unprecedented and exciting ways.
                    Our long-term goal is to establish a unique, interdisciplinary centre that will foster collaboration with research institutions world wide and will offer MSc and PhD courses, training a new generation of scholars that will bridge the divide between the arts and the sciences.
